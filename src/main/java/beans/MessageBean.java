package beans;

public class MessageBean {
	
	private String username_client;
	private String username_docteur;
	private String message;
	
	
	
	public String getUsername_client() {
		return username_client;
	}
	public void setUsername_client(String username_client) {
		this.username_client = username_client;
	}
	public String getUsername_docteur() {
		return username_docteur;
	}
	public void setUsername_docteur(String username_docteur) {
		this.username_docteur = username_docteur;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	
	
	
	
	
	

}
